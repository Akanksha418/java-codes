public class expression {
    public static void main(String[] args) {
        int number = 10;

        // Postfix increment
        System.out.println(number);  
        System.out.println(number++);
        System.out.println(number); 

        // Prefix decrement
        System.out.println(number);  
        System.out.println(number--); 
        System.out.println(number);  
    }
}
